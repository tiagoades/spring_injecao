package com.algaworks.di.model;

import java.math.BigDecimal;

public class Produto {

	private String nome;
	private BigDecimal valorTotal;

	public Produto() {

		super();
		this.nome = nome;
		this.valorTotal = valorTotal;

	}

	public String getNome() {
		return nome;
	}

	public BigDecimal getValorTotal() {
		return valorTotal;
	}

}
